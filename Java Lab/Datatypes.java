public class Datatypes{
    public static void main(String[] args){
        int a = 1;
        float f = 2.2f;
        double d = 3;
        char c = 'c';
        boolean b = true;

        System.out.println(a);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);

    }
}